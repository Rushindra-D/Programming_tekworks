#addition,subtraction,multliplication,division,modulo division
def add(a,b):
    c=a+b
    return c
def sub(a,b):
    d=a-b
    return d
def mul(a,b):
    return a*b
def div(a,b):
    return a/b
def modulo(a,b):
    return a%b
x=add(10,20)
y=sub(30,15)
z=mul(10,30)
w=div(9,3)
t=modulo(10,2)
print("addition:",x,"subtraction:",y,"multliplication",z,"division",w,"modulo",t)
